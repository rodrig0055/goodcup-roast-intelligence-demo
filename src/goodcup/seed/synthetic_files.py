def write_sample_fixtures(samples_dir: str | Path) -> dict:
    """Write the committed synthetic sample files used as parser fixtures.

    Deterministic (fixed RNG). Two Artisan profiles + one Cropster export, plus a
    cupping sheet scoring all three by two cuppers. Returns the paths written.
    """
    d = Path(samples_dir)
    d.mkdir(parents=True, exist_ok=True)
    rng = np.random.default_rng(7)

    specs = [
        dict(ref="R-2026-001", lot="Kochere Washed", machine="Probat-P12", roaster="Sam",
             date="2026-02-03", fmt="artisan",
             charge=204, tp_temp=91, tp_time=68, drop_temp=209, drop_time=588,
             dry_end=298, fc_start=474, fc_end=534),
        dict(ref="R-2026-002", lot="Sidama Natural", machine="Probat-P12", roaster="Mia",
             date="2026-02-04", fmt="artisan",
             charge=200, tp_temp=94, tp_time=74, drop_temp=213, drop_time=612,
             dry_end=320, fc_start=498, fc_end=560),
        dict(ref="R-2026-003", lot="Huila Pink Bourbon", machine="Loring-S15", roaster="Sam",
             date="2026-02-05", fmt="cropster",
             charge=198, tp_temp=90, tp_time=66, drop_temp=208, drop_time=602,
             dry_end=310, fc_start=486, fc_end=548),
    ]

    written = {"artisan": [], "cropster": [], "cupping": []}
    for s in specs:
        times, bt, et = make_roast_curve(
            charge_temp=s["charge"], tp_temp=s["tp_temp"], tp_time=s["tp_time"],
            drop_temp=s["drop_temp"], drop_time=s["drop_time"], dt=2.0, rng=rng,
        )
        events = CurveEvents(s["dry_end"], s["fc_start"], s["fc_end"], s["drop_time"])
        common = dict(
            times=times, bt=bt, et=et, events=events, lot_name=s["lot"],
            machine_id=s["machine"], roaster_name=s["roaster"], roast_ref=s["ref"],
            roast_date=s["date"], temp_unit="C",
        )
        if s["fmt"] == "artisan":
            p = write_artisan_alog(d / f"sample_artisan_{s['ref']}.alog", **common)
            written["artisan"].append(p)
        else:
            p = write_cropster_csv(d / f"sample_cropster_{s['ref']}.csv", **common)
            written["cropster"].append(p)

    # a cupping sheet: two cuppers score all three roasts (mixed nothing -- all SCA here)
    cup_rows = []
    base = {"R-2026-001": 86.0, "R-2026-002": 84.5, "R-2026-003": 85.25}
    descriptors = {
        "R-2026-001": "jasmine, bergamot, lemon, black tea, honey",
        "R-2026-002": "blueberry, strawberry, cocoa, winey",
        "R-2026-003": "red apple, caramel, orange, floral",
    }
    for ref, total in base.items():
        for cupper, delta in (("Ana", 0.0), ("Ben", -0.75)):
            cup_rows.append({
                "roast_ref": ref, "cupper_name": cupper, "cupping_date": "2026-02-10",
                "session_id": "SES-2026-02", "form_type": "sca_traditional",
                "fragrance_aroma": 7.75, "flavor": 8.0, "aftertaste": 7.5, "acidity": 8.0,
                "body": 7.5, "balance": 7.75, "uniformity": 10, "clean_cup": 10,
                "sweetness": 10, "overall": 7.75 + delta, "defect_points": 0,
                "total_score": round(total + delta, 2),
                "descriptors_raw": descriptors[ref], "notes": "",
            })
    p = write_cupping_csv(d / "sample_cupping_SES-2026-02.csv", cup_rows)
    written["cupping"].append(p)
    return written
