"""Builders for realistic *synthetic* roast/cupping files.

There were no real Artisan/Cropster/cupping files available for this prototype, so
these produce plausible fixtures that the real parsers can round-trip. They are
clearly labelled synthetic. If real client files arrive, verify the parsers against
the actual bytes (PRD sections 0 and 8) -- field names in particular drift between
Artisan versions.

The same :func:`make_roast_curve` also feeds the bulk seed generator, so demo data
and parser fixtures share one physically-plausible curve model.
"""

from __future__ import annotations

import csv
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

import numpy as np


@dataclass
class CurveEvents:
    """Event times in seconds from CHARGE."""

    dry_end_time_s: float
    fc_start_time_s: float
    fc_end_time_s: float
    drop_time_s: float


def make_roast_curve(
    *,
    charge_temp: float = 204.0,
    tp_temp: float = 92.0,
    tp_time: float = 70.0,
    drop_temp: float = 210.0,
    drop_time: float = 590.0,
    dt: float = 2.0,
    rng: Optional[np.random.Generator] = None,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Return ``(times, bt, et)`` from CHARGE (t=0) to DROP.

    BT falls linearly from ``charge_temp`` to the turning point, then rises with a
    decelerating slope (RoR high early, tapering) -- the classic roast shape. ET
    tracks a little above BT. Optional sensor noise via ``rng``.
    """
    times = np.arange(0.0, drop_time + dt, dt)
    bt = np.empty_like(times)
    for i, t in enumerate(times):
        if t <= tp_time:
            frac = t / tp_time if tp_time > 0 else 1.0
            bt[i] = charge_temp + (tp_temp - charge_temp) * frac
        else:
            x = (t - tp_time) / (drop_time - tp_time)
            bt[i] = tp_temp + (drop_temp - tp_temp) * (x ** 0.75)
    if rng is not None:
        bt = bt + rng.normal(0, 0.25, size=bt.shape)
    et = bt + 14.0 + (rng.normal(0, 0.4, size=bt.shape) if rng is not None else 0.0)
    return times, bt, et


def temp_at(times: np.ndarray, temps: np.ndarray, t: float) -> float:
    return float(np.interp(t, times, temps))


# --------------------------------------------------------------------------- #
# Artisan .alog  (Python dict literal, as Artisan writes it)
# --------------------------------------------------------------------------- #
def write_artisan_alog(
    path: str | Path,
    *,
    times: np.ndarray,
    bt: np.ndarray,
    et: np.ndarray,
    events: CurveEvents,
    lot_name: str,
    machine_id: str,
    roaster_name: str,
    roast_ref: str,
    roast_date: str,
    temp_unit: str = "C",
    pre_charge_s: float = 20.0,
    dt: float = 2.0,
) -> Path:
    """Write a synthetic Artisan profile. Includes a short pre-charge warmup so the
    CHARGE index is non-zero (unambiguous), mirroring a real recording."""
    n_pre = int(round(pre_charge_s / dt))
    pre_t = np.arange(0.0, pre_charge_s, dt)
    pre_bt = np.full(pre_t.shape, float(bt[0]))
    pre_et = np.full(pre_t.shape, float(et[0]))

    timex = np.concatenate([pre_t, times + pre_charge_s])
    temp2 = np.concatenate([pre_bt, bt])   # BT
    temp1 = np.concatenate([pre_et, et])   # ET

    def idx(t_from_charge: float) -> int:
        return int(round((t_from_charge + pre_charge_s) / dt))

    # timeindex = [CHARGE, DRYEND, FCstart, FCend, SCstart, SCend, DROP, COOL]
    timeindex = [
        n_pre,
        idx(events.dry_end_time_s),
        idx(events.fc_start_time_s),
        idx(events.fc_end_time_s),
        0,  # SC start (not reached)
        0,  # SC end
        idx(events.drop_time_s),
        0,  # cool (not recorded)
    ]

    profile = {
        "version": "2.8.10",
        "mode": temp_unit,
        "title": roast_ref,
        "beans": lot_name,
        "roastdate": roast_date,
        "roastertype": machine_id,
        "operator": roaster_name,
        "timex": [round(float(x), 1) for x in timex],
        "temp1": [round(float(x), 1) for x in temp1],
        "temp2": [round(float(x), 1) for x in temp2],
        "timeindex": timeindex,
        "_note": "SYNTHETIC fixture generated by GoodCup seed.synthetic_files",
    }
    path = Path(path)
    path.write_text(repr(profile), encoding="utf-8")
    return path


# --------------------------------------------------------------------------- #
# Cropster CSV  (metadata # lines + time series with an event column)
# --------------------------------------------------------------------------- #
def write_cropster_csv(
    path: str | Path,
    *,
    times: np.ndarray,
    bt: np.ndarray,
    et: np.ndarray,
    events: CurveEvents,
    lot_name: str,
    machine_id: str,
    roaster_name: str,
    roast_ref: str,
    roast_date: str,
    temp_unit: str = "C",
) -> Path:
    """Write a synthetic Cropster-style CSV. Time is seconds from CHARGE; event
    labels sit in an ``event`` column on the nearest sample row."""
    path = Path(path)
    event_at = {
        int(round(events.dry_end_time_s)): "Dry End",
        int(round(events.fc_start_time_s)): "First Crack",
        int(round(events.fc_end_time_s)): "First Crack End",
        int(round(events.drop_time_s)): "Drop",
    }
    with path.open("w", encoding="utf-8", newline="") as fh:
        fh.write("# Cropster Roast Export (SYNTHETIC)\n")
        fh.write(f"# Batch: {roast_ref}\n")
        fh.write(f"# Green: {lot_name}\n")
        fh.write(f"# Machine: {machine_id}\n")
        fh.write(f"# Roaster: {roaster_name}\n")
        fh.write(f"# Date: {roast_date}\n")
        fh.write(f"# Unit: {temp_unit}\n")
        writer = csv.writer(fh)
        writer.writerow(["time_s", "bean_temp", "env_temp", "event"])
        writer.writerow([0, round(float(bt[0]), 1), round(float(et[0]), 1), "Charge"])
        for t, b, e in zip(times, bt, et):
            ti = int(round(float(t)))
            if ti == 0:
                continue  # charge row already written
            writer.writerow([ti, round(float(b), 1), round(float(e), 1), event_at.get(ti, "")])
    return path


# --------------------------------------------------------------------------- #
# Cupping CSV  (matches templates/cupping_entry.csv)
# --------------------------------------------------------------------------- #
CUPPING_COLUMNS = [
    "roast_ref", "cupper_name", "cupping_date", "session_id", "form_type",
    "fragrance_aroma", "flavor", "aftertaste", "acidity", "body", "balance",
    "uniformity", "clean_cup", "sweetness", "overall", "defect_points",
    "total_score", "descriptors_raw", "notes",
]


def write_cupping_csv(path: str | Path, rows: list[dict]) -> Path:
    path = Path(path)
    with path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=CUPPING_COLUMNS, extrasaction="ignore")
        writer.writeheader()
        for r in rows:
            writer.writerow(r)
    return path


